    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        try{
            int id = Integer.valueOf(txtid.getText());
            int type = map.get(cmbtype.getSelectedItem().toString());
            int ownerid = Integer.valueOf(txtownerid.getText());
            int squarefeet = Integer.valueOf(txtfeet.getText());
            int bedrooms = Integer.valueOf(spnbedrooms.getValue().toString());
            int bathrooms = Integer.valueOf(spnbathrooms.getValue().toString());
            int age = Integer.valueOf(spnage.getValue().toString());
            
            boolean haveBalcony = false;
            boolean havePool = false;
            boolean haveBackyard = false;
            boolean haveGarage = false;
            
            if(cbxbalcony.isSelected()) haveBalcony = true;
            if(cbxpool.isSelected()) havePool = true;
            if(cbxbackyard.isSelected()) haveBackyard = true;
            if(cbxgarage.isSelected()) haveGarage = true;
            
            String price = txtprice.getText();
            String address = txtaddress.getText();
            String description = txtdes.getText();
            
            P_Properties property = new P_Properties(id, type, squarefeet, ownerid, price, address, bedrooms, bathrooms, age, haveBalcony, havePool, haveBackyard, haveGarage, description);
            
            if(new P_Properties().updateProperty(property))
            {
                JOptionPane.showMessageDialog(rootPane, "Property data updated..", "Update Property", 1);
            }
            else {
                JOptionPane.showMessageDialog(rootPane, "Property data not updated..", "Update Property", 2);
            }
            
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(rootPane, "Enter a valid property ID & owner ID..","Invalid data" , 0);
        }
        
        //clear all fields after updating
        txtid.setText("");
        txtfeet.setText("");
        txtownerid.setText("");
        txtprice.setText("");
        txtaddress.setText("");
        txtdes.setText("");
        cmbtype.setSelectedIndex(0);

        spnbedrooms.setValue(0);
        spnbathrooms.setValue(0);
        spnage.setValue(0);

        cbxbalcony.setSelected(false);
        cbxpool.setSelected(false);
        cbxbackyard.setSelected(false);
        cbxgarage.setSelected(false);

        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed

         try{
            //deleting the selected Property from the system
            int id = Integer.valueOf(txtid.getText());

            P_Properties property = new P_Properties();

            if (txtid.getText().trim().equals("")) {

                JOptionPane.showMessageDialog(rootPane, "Enter the property ID..", "Empty ID", 2);

            } else {
                //show a confirmation message before deleting the property
                int yes_or_no = JOptionPane.showConfirmDialog(rootPane, "Are you sure to delete this property", "Delete property", JOptionPane.YES_NO_OPTION);

                if (yes_or_no == JOptionPane.YES_OPTION) {

                    if (property.removeProperty(id)) {

                        JOptionPane.showMessageDialog(null, "Property data deleted..", "Delete Property", 1);
                        
                        //clear all fields after deleting the property
                        txtid.setText("");
                        txtfeet.setText("");
                        txtownerid.setText("");
                        txtprice.setText("");
                        txtaddress.setText("");
                        txtdes.setText("");
                        cmbtype.setSelectedIndex(0);
                        
                        spnbedrooms.setValue(0);
                        spnbathrooms.setValue(0);
                        spnage.setValue(0);
                
                        cbxbalcony.setSelected(false);
                        cbxpool.setSelected(false);
                        cbxbackyard.setSelected(false);
                        cbxgarage.setSelected(false);
                        
                    } else {
                        JOptionPane.showMessageDialog(null, "Operation failed..", "Delete Property", 2);
                    }
                }

            }

        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(rootPane, "Enter a valid ID..","Invalid ID" , 0);
        }
        
    }//GEN-LAST:event_btnRemoveActionPerformed

