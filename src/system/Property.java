    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        
        //uncheck the checkboxes
        cbxbalcony.setSelected(false);
        cbxpool.setSelected(false);
        cbxbackyard.setSelected(false);
        cbxgarage.setSelected(false);
        
        //search property by id and display the property data
        try{
            int propertyId = Integer.valueOf(txtid.getText());
            
            P_Properties property = new P_Properties().searchProperty(propertyId);
            
            if(property != null)
            {
                txtfeet.setText(String.valueOf(property.getSize()));
                txtownerid.setText(String.valueOf(property.getOwnerid()));
                txtprice.setText(String.valueOf(property.getPrice()));
                txtaddress.setText(String.valueOf(property.getAddress()));
                txtdes.setText(String.valueOf(property.getDescription()));
                
                for(Object type : map.keySet())
                {
                    if(map.get(type).equals(property.getType()))
                        cmbtype.setSelectedItem(type);
                }
                
                spnbedrooms.setValue(property.getBedrooms());
                spnbathrooms.setValue(property.getBathrooms());
                spnage.setValue(property.getAge());
                
                if(property.isBalcony()) cbxbalcony.setSelected(true);
                if(property.isPool()) cbxpool.setSelected(true);
                if(property.isBackyard()) cbxbackyard.setSelected(true);
                if(property.isGarage()) cbxgarage.setSelected(true);
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Property not found..","Invalid ID" , 0);
            }
            
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(rootPane, "Enter a valid ID..","Invalid ID" , 0);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

